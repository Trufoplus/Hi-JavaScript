// Imprimiendo números por consola
console.log(5);        // Imprime el número 5
console.log('5');      // Imprime el string '5' (entre comillas)
console.log(5 + 5);    // Imprime el resultado de la suma: 10
console.log(5 - 5);    // Imprime el resultado de la resta: 0
console.log(5 * 5);    // Imprime el resultado de la multiplicación: 25
console.log(5 / 5);    // Imprime el resultado de la división: 1
console.log(5 % 5);    // Imprime el resultado del módulo (resto de la división): 0
console.log(5 ** 5);   // Imprime el resultado de la potencia: 3125